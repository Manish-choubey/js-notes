//The switch statement is used to perform different actions based on different conditions.

let day = 2

switch(day){
    case 0 :
        console.log("sunday")
        break;
        case 1:
        console.log("monday")
        break;
        case 2:
         console.log("tueesday")
         break;
         default: 
         console.log("no day")  
}